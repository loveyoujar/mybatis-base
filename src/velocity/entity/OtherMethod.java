package velocity.entity;

/**
 * 其他方法集合
 * @author neo
 * @since 2013-01-01
 */
public class OtherMethod {
	/**
	 * 其他表名
	 */
	private String otherTableName;

	/**
	 * 其他表主键
	 */
	private String otherPkName;

	/**
	 * 其他方法后缀
	 */
	private String otherMethodSuffix;

	/**
	 * 方法注释关键字
	 */
	private String otherMethodName;

	public String getOtherMethodSuffix() {
		return otherMethodSuffix;
	}

	public void setOtherMethodSuffix(String otherMethodSuffix) {
		this.otherMethodSuffix = otherMethodSuffix;
	}

	public String getOtherTableName() {
		return otherTableName;
	}

	public void setOtherTableName(String otherTableName) {
		this.otherTableName = otherTableName;
	}

	public String getOtherPkName() {
		return otherPkName;
	}

	public void setOtherPkName(String otherPkName) {
		this.otherPkName = otherPkName;
	}

	public String getOtherMethodName() {
		return otherMethodName;
	}

	public void setOtherMethodName(String otherMethodName) {
		this.otherMethodName = otherMethodName;
	}

}
